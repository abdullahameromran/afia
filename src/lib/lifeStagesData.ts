export const getLifeStageFromAge = (age: number): LifeStage | null => {
  if (isNaN(age) || age <= 0) return null;
  for (const stage of lifeStagesData) {
    if (age >= stage.minAge && age <= stage.maxAge) {
      return stage;
    }
  }
  // If age is very high and above all defined maxAges, but our last stage covers "and older"
  const lastStage = lifeStagesData[lifeStagesData.length - 1];
  if (lastStage && age > lastStage.maxAge && lastStage.maxAge === 120) { // Assuming 120 is effectively "and older"
      // This case should be caught by the loop if maxAge is high.
      // This check might be redundant if the last stage's maxAge is set very high.
  }
  return null; 
};
